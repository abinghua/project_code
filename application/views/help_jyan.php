        <p>骗术二：伪造电子邮件。假冒银行发送虚假邮件，以受害人帐户被冻结、系统升级等借口，要求受害人点击邮件里的联接地址，诱骗受害人登陆伪造的银行网站，输入帐户机密信息。</p>
